package generator

import (
	"embed"
	"fmt"
	"io/fs"
	"os"
	"path/filepath"
	"strings"
)

//go:embed static/*
var staticFiles embed.FS

// generateStatic copies embedded static files to output directory
// preserving the directory structure
func (g *Generator) generateStatic(outputDir string) error {
	return fs.WalkDir(staticFiles, "static", func(path string, d fs.DirEntry, err error) error {
		if err != nil {
			return err
		}

		// Skip the root "static" directory itself
		if path == "static" {
			return nil
		}

		relPath := strings.TrimPrefix(path, "static/")
		dstPath := filepath.Join(outputDir, relPath)

		if d.IsDir() {
			return os.MkdirAll(dstPath, 0755)
		}

		if filepath.Ext(dstPath) != ".go" {
			// Skip non-Go files
			return nil
		}

		dstPath = strings.TrimSuffix(dstPath, ".go")

		if strings.HasSuffix(filepath.Base(dstPath), "_test") {
			// Skip test files
			return nil
		}

		dstPath = dstPath + ".gen.go"

		content, err := staticFiles.ReadFile(path)
		if err != nil {
			return fmt.Errorf("failed to read embedded file %s: %w", path, err)
		}

		content = append([]byte("// Code generated by scalr-gen. DO NOT EDIT.\n\n"), content...)

		// Ensure parent directory exists
		if err := os.MkdirAll(filepath.Dir(dstPath), 0755); err != nil {
			return fmt.Errorf("failed to create directory for %s: %w", dstPath, err)
		}

		if err := os.WriteFile(dstPath, content, 0644); err != nil {
			return fmt.Errorf("failed to write file %s: %w", dstPath, err)
		}

		return nil
	})
}
